class LexemaGeneral():

    def __init__(self, lexemaG, fila, columna):
        self.lexemaG = lexemaG
        self.fila=fila
        self.columna=columna
    
    def operatoriaConRecursividad(self):
        return self.lexemaG
 

    def getFila(self):
        return self.fila
    
    def getColumna(self):
        return self.columna
    
    def getLexema(self):
        return self.lexemaG

